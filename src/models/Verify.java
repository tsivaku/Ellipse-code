package models;

public class Verify {

	public double rdx;
	public double rdy;
	public double rdz;
	public String status;
	public double getRdx() {
		return rdx;
	}
	public void setRdx(double rdx) {
		this.rdx = rdx;
	}
	public double getRdy() {
		return rdy;
	}
	public void setRdy(double rdy) {
		this.rdy = rdy;
	}
	public double getRdz() {
		return rdz;
	}
	public void setRdz(double rdz) {
		this.rdz = rdz;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
